#!/usr/bin/env node
import fs from "node:fs/promises";
import path from "node:path";
import { bundledPath, readCatalog, validateCatalog } from "./catalog-utils.mjs";

const catalog = await readCatalog();
const errors = validateCatalog(catalog);
if (errors.length > 0) {
  console.error(errors.join("\n"));
  process.exit(1);
}
await fs.mkdir(path.dirname(bundledPath), { recursive: true });
const contents = `// Generated by scripts/build-bundled-catalog.mjs. Do not edit manually.\nimport type { CatalogFile } from "./types";\n\nexport const BUNDLED_CATALOG: CatalogFile = ${JSON.stringify(catalog, null, 2)};\n`;
await fs.writeFile(bundledPath, contents);
console.log(`Bundled catalog generated: ${catalog.items.length} items.`);
